for(let i of news.phase1){
    


    let image = document.createElement('img')
    image.classList.add("img-news")
    image.setAttribute('src',i.image)

    let divanh = document.createElement('div')
    divanh.classList.add('divanh')
    divanh.appendChild(image) 

    let divlon = document.createElement("div")
    divlon.classList.add("divlon")
    divlon.innerText = i.tittle


    let divnho = document.createElement('div')
    divnho.classList.add("divnho")
    divnho.innerText = i.content

    let date = document.createElement('div')
    date.classList.add('date')
    date.innerText = i.date

    let divtext = document.createElement('div')
    divtext.classList.add("divtext")
    divtext.appendChild(divlon)
    divtext.appendChild(divnho)
    divtext.appendChild(date)

    let container = document.createElement('div')
    container.classList.add("container")
    container.appendChild(divtext)

    let  link = document.createElement('a')
    link.setAttribute('href',i.link)
    link.classList.add("ctn")
    link.appendChild(divanh)
    link.appendChild(container)
    

    document.getElementById("news-info").appendChild(link)
}

