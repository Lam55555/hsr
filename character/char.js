for(let i of charlist.char) {
    let icon = document.createElement('img')
    icon.classList.add("iconchar")
    icon.setAttribute('src',i.icon)

    let linkicon = document.createElement('a')
    linkicon.setAttribute('href',i.linkchar)
    linkicon.appendChild(icon)
    linkicon.classList.add("linkicon")

    let namechar = document.createElement("a")
    namechar.innerText = i.name
    namechar.classList.add("pnamechar")
    namechar.setAttribute('href',i.linkchar)

    let linkchar = document.createElement('div')
    linkchar.classList.add('linkchar')
    linkchar.appendChild(namechar)

    let divicchar = document.createElement('div')
    divicchar.classList.add('divicchar')
    divicchar.appendChild(linkicon)
    divicchar.appendChild(linkchar)

    let rate = document.createElement("img")
    rate.setAttribute('src',i.rarity)
    rate.classList.add("rate")

    let divrate = document.createElement('div')
    divrate.classList.add('divrate')
    divrate.appendChild(rate)

    let path = document.createElement("img")
    path.setAttribute("src",i.path)
    path.classList.add("path")

    let linkpath = document.createElement('a')
    linkpath.setAttribute('href',i.linkpath)
    // linkpath.classList.add('linkpath')
    linkpath.appendChild(path)

    let namepath = document.createElement('a')
    namepath.classList.add('namepath')
    namepath.innerText = i.namepath
    namepath.setAttribute('href',i.linkpath)

    let divpath = document.createElement('div')
    divpath.classList.add('divpath')
    divpath.appendChild(linkpath)
    divpath.appendChild(namepath)

    let idmg = document.createElement('img')
    idmg.classList.add('idmg')
    idmg.setAttribute('src',i.cbt)

    let linkdmg = document.createElement('a')
    linkdmg.setAttribute('href',i.linkdmg)
    linkdmg.classList.add('linkdmg')
    linkdmg.appendChild(idmg)

    let typedmg = document.createElement('p')
    typedmg.classList.add('typedmg')
    typedmg.innerText = i.typecbt


    if(typedmg == "Hỏa"){
        typedmg.classList.add('red')
    }


    let container = document.createElement('div')
    container.classList.add("ctn")
    container.appendChild(divicchar)
    container.appendChild(divrate)
    container.appendChild(divpath)
    container.appendChild(linkdmg)
    container.appendChild(typedmg)


    document.getElementById("showchar").appendChild(container)
}
